#Arithmetic Operators
x = 20
y = 6

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)   #Modulus - divides the two numbers then it returns the remainder.


#Comparison Operators -Comparing values
print (x < y)
print (x > y)
print (x <= y)
print (x >= y)
print (x == y)    #Equal to
print (x != y)    #Not Equal to


#Assignment Operators -- Used to assign/ store values in variables
number = 45
print(number)

number +=2
print(number)

number -=2
print(number)

